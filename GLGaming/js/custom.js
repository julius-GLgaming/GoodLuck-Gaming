function openGamingDropdown() {
    document.getElementById("gamingSessions").classList.toggle("show");
    var dropdowns = document.getElementsByClassName("dota-content");
    var i;
    for (i = 0; i < dropdowns.length; i++) {
      var openDropdown = dropdowns[i];
      if (openDropdown.classList.contains('show')) {
        openDropdown.classList.remove('show');
      }
    }
}

